# Tuplas
my_tuple = (1, 2, 3, 4, 5)
print (my_tuple)
print (type(my_tuple)) # Vemos el tipo de dato que tenemos
print (my_tuple[0]) # Accedemos al índice 0 de la tupla
print (my_tuple.count(2)) # Nos dice cuantas veces se repìte el elemento dado
print (my_tuple.index(3)) # Nos dice el índice del elemento dado
'''Las tuplas son valores inmutables y no se pueden cambiar'''
my_other_tuple = ('Perro', 'Gato')
print (my_tuple + my_other_tuple) # Concatenamos las dos tuplas





