# Operadores aritméticos
print (3 + 4) # Operador de sumas
print (2 - 4) # Operador de resta
print (3 / 4) # Operador de división
print (3 * 4) # Operador de multiplicación
print (3 % 4) # Operador de módulo
print (3 ** 4) # Operador de potencia (exponente)

# Con los operadores aritméticos también podemos hacer concatenaciones
variable_1 = 'Hola qué tal '
variable_2 = 'estás'
print (variable_1 + variable_2)
print ('Hola qué tal estás número ', str(5)) # Concatenamos convirtiendo el entero en string
print ('Hola ' * 5 )

# Operadores comparativos
print (3 > 4) # Mayor qué
print (3 < 4) # Menor qué
print (3 <= 4) # Menor o igual qué
print (3 >= 4) # Mayor o igual qué
print (3 == 4) # Comparador de igualdad
print (3 != 4) # Diferente qué
